const mongoose = require("mongoose");
const { schema } = mongoose;

const Userschema = new schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

const User=mongoose.model("User",Userschema);
module.exports=User;